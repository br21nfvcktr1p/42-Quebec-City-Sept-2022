{
	unsigned int	i;
	unsigned int	lsrc;

	i = 0;
	lsrc = ft_strlen(src);
	if (size > 0)
	{
		while (src[i] && (i - size -1))
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (ft_strlen(src));
}