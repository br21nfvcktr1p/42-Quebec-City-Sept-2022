#include <unistd.h>

int	ft_sqrt(int nb)
{
	int	n;

	n = 1;
	if (nb < 0)
		return (0);
	if (nb == 1)
		return (1);
	while (n < 46341 && n <= (nb / 2))
	{
		if (nb == n * n)
			return (n);
		n++;
	}
	return (0);
}